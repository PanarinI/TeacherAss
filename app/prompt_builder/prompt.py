def _get_level_match_comment(level):
    comments = {
        'below': "–º–∞—Ç–µ—Ä–∏–∞–ª –ø—Ä–æ—â–µ —É—Ä–æ–≤–Ω—è —É—á–µ–Ω–∏–∫–æ–≤ - –¥–æ–±–∞–≤—å —É—Å–ª–æ–∂–Ω—ë–Ω–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã",
        'on-level': "–º–∞—Ç–µ—Ä–∏–∞–ª —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç —É—Ä–æ–≤–Ω—é —É—á–µ–Ω–∏–∫–æ–≤",
        'above': "–º–∞—Ç–µ—Ä–∏–∞–ª —Å–ª–æ–∂–Ω–µ–µ —É—Ä–æ–≤–Ω—è —É—á–µ–Ω–∏–∫–æ–≤ - –ø—Ä–µ–¥—É—Å–º–æ—Ç—Ä–∏ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –æ–±—ä—è—Å–Ω–µ–Ω–∏—è",
        'mixed': "–≤ –≥—Ä—É–ø–ø–µ —Ä–∞–∑–Ω—ã–µ —É—Ä–æ–≤–Ω–∏ - –ø–æ–¥–≥–æ—Ç–æ–≤—å –¥–∏—Ñ—Ñ–µ—Ä–µ–Ω—Ü–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –∑–∞–¥–∞–Ω–∏—è"
    }
    return comments.get(level, "—É—á—Ç–∏ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ —É—Ä–æ–≤–Ω—è –º–∞—Ç–µ—Ä–∏–∞–ª–∞ –∏ —É—á–µ–Ω–∏–∫–æ–≤")

def _get_age_group_comment(age):
    try:
        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —Å—Ç—Ä–æ–∫—É –Ω–∞ —Å–ª—É—á–∞–π, –µ—Å–ª–∏ –≤–æ–∑—Ä–∞—Å—Ç –ø—Ä–∏—à—ë–ª –∫–∞–∫ —á–∏—Å–ª–æ
        age_str = str(age).strip()

        # –£–¥–∞–ª—è–µ–º –≤—Å–µ –Ω–µ—Ü–∏—Ñ—Ä–æ–≤—ã–µ —Å–∏–º–≤–æ–ª—ã, –∫—Ä–æ–º–µ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–µ–π (–ø—Ä–æ–±–µ–ª, –¥–µ—Ñ–∏—Å, –∑–∞–ø—è—Ç–∞—è)
        cleaned = ''.join(c if c.isdigit() or c in ' -,' else ' ' for c in age_str)

        # –†–∞–∑–±–∏–≤–∞–µ–º –ø–æ –≤–æ–∑–º–æ–∂–Ω—ã–º —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—è–º
        for separator in ['-', ' ', ',']:
            if separator in cleaned:
                age_parts = cleaned.split(separator)
                # –ë–µ—Ä—ë–º –ø–µ—Ä–≤—ã–π –ø–æ–ø–∞–≤—à–∏–π—Å—è —á–∏—Å–ª–æ–≤–æ–π —ç–ª–µ–º–µ–Ω—Ç
                for part in age_parts:
                    if part.strip().isdigit():
                        age_int = int(part.strip())
                        break
                break
        else:
            # –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–µ–π - –ø—Ä–æ–±—É–µ–º –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞—Ç—å —Ü–µ–ª–∏–∫–æ–º
            age_int = int(cleaned) if cleaned.isdigit() else 0

        # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –≤–æ–∑—Ä–∞—Å—Ç–Ω—É—é –≥—Ä—É–ø–ø—É
        if 6 <= age_int <= 12:
            return "–¥–µ—Ç–∏: —Ñ–æ–∫—É—Å –Ω–∞ –∏–≥—Ä–∞—Ö, –¥–≤–∏–∂–µ–Ω–∏–∏, –≤–∏–∑—É–∞–ª–∞—Ö, –∫–æ—Ä–æ—Ç–∫–∏—Ö –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—è—Ö"
        elif 13 <= age_int <= 17:
            return "–ø–æ–¥—Ä–æ—Å—Ç–∫–∏: –∞–∫—Ç—É–∞–ª—å–Ω—ã–µ —Ç–µ–º—ã (–Ω–∞–ø—Ä. —Å–æ—Ü—Å–µ—Ç–∏, –∏–≥—Ä—ã), –≥—Ä—É–ø–ø–æ–≤—ã–µ –ø—Ä–æ–µ–∫—Ç—ã, —ç–ª–µ–º–µ–Ω—Ç —Å–æ—Ä–µ–≤–Ω–æ–≤–∞–Ω–∏—è"
        elif age_int > 17:
            return "–≤–∑—Ä–æ—Å–ª—ã–µ: –∫–µ–π—Å—ã –∏–∑ —Ä–µ–∞–ª—å–Ω–æ–π –∂–∏–∑–Ω–∏, –ø—Ä–æ—Ñ–µ—Å—Å–∏–æ–Ω–∞–ª—å–Ω—ã–µ –∫–æ–Ω—Ç–µ–∫—Å—Ç—ã, –¥–∏—Å–∫—É—Å—Å–∏–∏"
        else:
            return "–¥–æ—à–∫–æ–ª—å–Ω–∏–∫–∏: –∏–≥—Ä–æ–≤–æ–µ –æ–±—É—á–µ–Ω–∏–µ, –º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –Ω–∞–≥–ª—è–¥–Ω–æ—Å—Ç—å, —á–∞—Å—Ç–∞—è —Å–º–µ–Ω–∞ –¥–µ—è—Ç–µ–ª—å–Ω–æ—Å—Ç–∏"

    except (ValueError, AttributeError, TypeError):
        return "—É—á—Ç–∏ –≤–æ–∑—Ä–∞—Å—Ç–Ω—ã–µ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏ –≥—Ä—É–ø–ø—ã"



